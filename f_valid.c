#include "libft.h"

int		f_valid(char *str)
{
	int		i;

	i = 0;
	if (!str)
		return (0);
	if (ft_strlen(str) > 5)
		return (0);
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9' || str[0] == '0')
			return (0);
		else
			i++;
	}
	return (1);
}
